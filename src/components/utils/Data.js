export const reducer = (state, action) => {
  if (action.type === "COMPLETE") {
    return state.map((todo) => {
      if (todo.id === action.id) {
        return { ...todo, complete: !todo.complete };
      } else {
        return todo;
      }
    });
  }
  if (action.type === "RESET") {
    return state.map((todo) => ({ ...todo, complete: false }));
  }
  return state;
};
